// הדפסת יום

var day = Number(prompt ("enter num from 1 to 7"))

 switch (day) {
    case 1:
        console.log("יום ראשון")
        break;
    case 2:
        console.log("יום שני")
        break;
    case 3:
        console.log("יום שלישי")
        break;
    case 4:
        console.log("יום רביעי")
        break;
    case 5:
        console.log("יום חמישי")
        break;
    case 6:
        console.log("יום שישי")
        break;
    case 7:
        console.log("יום שבת")
        break;

 }
